package by.kino.jmp.beans;

public class Film {
	private int ticketPrice;
	private String name;
	
	public Film(int ticketPrice, String name) {
		super();
		this.ticketPrice = ticketPrice;
		this.name = name;
	}

	public int getTicketPrice() {
		return ticketPrice;
	}

	public void setTicketPrice(int ticketPrice) {
		this.ticketPrice = ticketPrice;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ticketPrice;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Film other = (Film) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (ticketPrice != other.ticketPrice)
			return false;
		return true;
	}
	
	@Override
	public String toString() {
		return "Film [ticketPrice=" + ticketPrice
				+ ", name=" + name + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
